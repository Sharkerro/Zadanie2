public class Choose_Figure {
    static int number;
    public  int choose(int number)
    {
        if(number==1)
        {
            return 1;
        }
        else
        if(number==2)
        {
            return 2;
        }
        else
        if(number==3)
        {
            return 3;
        }
        else
            return 0;
    }

}


